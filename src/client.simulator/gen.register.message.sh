#!/bin/bash
# 该脚本从 proto/user.proto 的 //0x... 注释中提取客户端协议消息，并生成 main/register.message.go。

# 获取当前脚本文件所在路径的绝对路径
currentPath=$(realpath "$(dirname "$0")")
echo "currentPath:${currentPath}"

# 模拟器目录
simulatorPath="${currentPath}"
echo "simulatorPath:${simulatorPath}"

# server 仓库根目录
serverPath=$(dirname "$(dirname "${simulatorPath}")")
echo "serverPath:${serverPath}"

# 可传入 proto 目录；默认使用 server/proto
protoPath="${1:-${serverPath}/proto}"
inputProto="${protoPath}/user.proto"
outputGo="${simulatorPath}/main/register.message.go"

if [ ! -f "${inputProto}" ]; then
    echo "输入文件 ${inputProto} 不存在"
    exit 1
fi

tempFile=$(mktemp)

cat > "${tempFile}" << 'EOF'
// Code generated by gen.register.message.sh. DO NOT EDIT.

package main

import (
	pb "server/proto/pb"

	xpacket "github.com/75912001/xlib/packet"
	"google.golang.org/protobuf/proto"
)

type MessageDef struct {
	ID       uint32                                        // 消息号。
	Name     string                                        // 日志展示用消息名。
	New      func() proto.Message                          // 创建空 Protobuf 消息。
	OnRecv   func(*Client, *xpacket.Header, proto.Message) // 收到该消息后的状态更新逻辑。
	IsNotify bool                                          // 是否为通知类消息，当前预留。
}

// messages 是模拟器的消息注册表；新增协议时运行 gen.register.message.sh 重新生成。
var messages = map[uint32]MessageDef{
EOF

awk '
	/^[[:space:]]*\/\/[[:space:]]*0x[0-9A-Fa-f]+#/ {
		comment=$0
		pending=1
		next
	}
	pending && /^[[:space:]]*message[[:space:]]+[A-Za-z0-9_]+/ {
		name=$2
		gsub(/\{/, "", name)
		print comment "\t" name
		pending=0
	}
' "${inputProto}" | while IFS=$'\t' read -r comment messageName; do
    [ -z "${messageName}" ] && continue

    cat >> "${tempFile}" << EOF
	${comment}
	uint32(pb.MsgIDUser_${messageName}_CMD): {
		ID:   uint32(pb.MsgIDUser_${messageName}_CMD),
		Name: "${messageName}",
		New:  func() proto.Message { return &pb.${messageName}{} },
EOF

    case "${messageName}" in
        UserVerifyRes)
            cat >> "${tempFile}" << 'EOF'
		OnRecv: func(c *Client, header *xpacket.Header, msg proto.Message) {
			res := msg.(*pb.UserVerifyRes)
			c.log("verify resultID=%d serverTime=%d", header.ResultID, res.GetServerTime())
		},
EOF
            ;;
        UserHeartbeatRes)
            cat >> "${tempFile}" << 'EOF'
		OnRecv: func(c *Client, header *xpacket.Header, msg proto.Message) {
			res := msg.(*pb.UserHeartbeatRes)
			c.nextSession = res.GetNextSession()
			c.log("heartbeat resultID=%d nextSession=%d serverTime=%d", header.ResultID, c.nextSession, res.GetServerTime())
		},
EOF
            ;;
    esac

    cat >> "${tempFile}" << 'EOF'
	},
EOF
done

cat >> "${tempFile}" << 'EOF'
}
EOF

mv "${tempFile}" "${outputGo}"
gofmt -w "${outputGo}"

echo "消息注册文件生成完成: ${outputGo}"
